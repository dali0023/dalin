     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function postStatus($id)
    {
        $post = Post::find($id);
        abort_if(!isset($post), 404);
        if ($post->is_published === 0) {
            $post->is_published =1;
        }else{
            $post->is_published = 0;
        }
        $post->save();
        return Redirect::route('posts.index');
    }

